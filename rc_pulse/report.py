from .analysis import MetricTrend, HealthScore


def format_value(value: float | None, unit: str = "") -> str:
    if value is None:
        return "N/A"
    if unit == "$":
        return f"${value:,.2f}"
    elif unit == "#":
        return f"{value:,.0f}"
    elif unit == "%":
        return f"{value:.1f}%"
    return f"{value:,.2f}"


def generate_terminal_report(
    trends: dict[str, MetricTrend],
    health: HealthScore,
    flags: list[str],
    recommendations: list[str],
    project_name: str = "",
    date_range: str = "",
) -> str:
    """Generate a rich terminal-friendly report."""
    lines = []
    lines.append("=" * 60)
    lines.append(f"  RC PULSE — Subscription Health Report")
    if project_name:
        lines.append(f"  Project: {project_name}")
    if date_range:
        lines.append(f"  Period: {date_range}")
    lines.append(f"  Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}")
    lines.append("=" * 60)
    lines.append("")

    # Health Score
    lines.append(f"  PULSE SCORE: {health.overall:.0f}/100 {health.grade_emoji} (Grade: {health.grade})")
    lines.append("")
    lines.append("  Dimensions:")
    for dim in ["growth", "retention", "conversion", "revenue_quality", "momentum"]:
        val = getattr(health, dim)
        bar_len = int(val / 5)
        bar = "█" * bar_len + "░" * (20 - bar_len)
        lines.append(f"    {dim.replace('_', ' ').title():20s} [{bar}] {val:.0f}")
    lines.append("")

    # Metric Summary
    lines.append("─" * 60)
    lines.append("  KEY METRICS")
    lines.append("─" * 60)
    for name, trend in trends.items():
        change_str = ""
        if trend.period_change is not None:
            arrow = "↑" if trend.period_change >= 0 else "↓"
            change_str = f" ({arrow} {abs(trend.period_change):.1f}%)"
        lines.append(
            f"  {trend.trend_emoji} {trend.display_name:30s} "
            f"{format_value(trend.current, trend.unit):>12s}{change_str}"
        )
    lines.append("")

    # Risk Flags
    if flags:
        lines.append("─" * 60)
        lines.append("  ⚠️  RISK FLAGS")
        lines.append("─" * 60)
        for flag in flags:
            lines.append(f"  • {flag}")
        lines.append("")

    # Recommendations
    if recommendations:
        lines.append("─" * 60)
        lines.append("  💡 RECOMMENDATIONS")
        lines.append("─" * 60)
        for rec in recommendations:
            lines.append(f"  {rec}")
            lines.append("")

    lines.append("=" * 60)
    lines.append("  Generated by RC Pulse — https://github.com/CleoSterlingAI/rc-pulse")
    lines.append("=" * 60)

    return "\n".join(lines)


def generate_markdown_report(
    trends: dict[str, MetricTrend],
    health: HealthScore,
    flags: list[str],
    recommendations: list[str],
    project_name: str = "",
    date_range: str = "",
) -> str:
    """Generate a Markdown formatted report."""
    lines = []
    lines.append("# RC Pulse — Subscription Health Report")
    lines.append("")
    if project_name:
        lines.append(f"**Project:** {project_name}")
    if date_range:
        lines.append(f"**Period:** {date_range}")
    lines.append(f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M')}")
    lines.append("")

    # Health Score
    lines.append(f"## Pulse Score: {health.overall:.0f}/100 {health.grade_emoji}")
    lines.append("")
    lines.append("| Dimension | Score |")
    lines.append("|-----------|-------|")
    for dim in ["growth", "retention", "conversion", "revenue_quality", "momentum"]:
        val = getattr(health, dim)
        lines.append(f"| {dim.replace('_', ' ').title()} | {val:.0f}/100 |")
    lines.append("")

    # Metrics
    lines.append("## Key Metrics")
    lines.append("")
    lines.append("| Metric | Current | Change | Trend |")
    lines.append("|--------|---------|--------|-------|")
    for name, trend in trends.items():
        change = f"{trend.period_change:+.1f}%" if trend.period_change is not None else "N/A"
        lines.append(
            f"| {trend.display_name} | {format_value(trend.current, trend.unit)} "
            f"| {change} | {trend.trend_emoji} {trend.trend_direction} |"
        )
    lines.append("")

    # Trend Details
    lines.append("## Metric Details")
    lines.append("")
    for name, trend in trends.items():
        lines.append(f"### {trend.display_name}")
        direction, count = trend.consecutive_direction
        lines.append(f"- **Current:** {format_value(trend.current, trend.unit)}")
        lines.append(f"- **Mean:** {format_value(trend.mean, trend.unit)}")
        if trend.std_dev is not None:
            lines.append(f"- **Std Dev:** {format_value(trend.std_dev, trend.unit)}")
        lines.append(f"- **Trend:** {trend.trend_emoji} {trend.trend_direction}")
        lines.append(f"- **Consecutive {direction}s:** {count}")
        lines.append("")

    # Risk Flags
    if flags:
        lines.append("## ⚠️ Risk Flags")
        lines.append("")
        for flag in flags:
            lines.append(f"- {flag}")
        lines.append("")

    # Recommendations
    if recommendations:
        lines.append("## 💡 Recommendations")
        lines.append("")
        for rec in recommendations:
            lines.append(f"- {rec}")
        lines.append("")

    lines.append("---")
    lines.append("*Generated by [RC Pulse](https://github.com/CleoSterlingAI/rc-pulse)*")

    return "\n".join(lines)


def generate_json_report(
    trends: dict[str, MetricTrend],
    health: HealthScore,
    flags: list[str],
    recommendations: list[str],
    project_name: str = "",
    date_range: str = "",
) -> str:
    """Generate a JSON report."""
    report = {
        "meta": {
            "project": project_name,
            "period": date_range,
            "generated_at": datetime.now().isoformat(),
            "tool": "rc-pulse",
            "version": "0.1.0",
        },
        "health_score": health.to_dict(),
        "metrics": {
            name: {
                "display_name": t.display_name,
                "current": t.current,
                "previous": t.previous,
                "period_change_pct": round(t.period_change, 2) if t.period_change else None,
                "mean": round(t.mean, 2) if t.mean else None,
                "trend": t.trend_direction,
                "unit": t.unit,
                "values": list(zip(t.dates, t.values)),
            }
            for name, t in trends.items()
        },
        "risk_flags": flags,
        "recommendations": recommendations,
    }
    return json.dumps(report, indent=2)


