def generate_html_report(
    trends: dict[str, MetricTrend],
    health: HealthScore,
    flags: list[str],
    recommendations: list[str],
    project_name: str = "",
    date_range: str = "",
) -> str:
    """Generate an HTML report."""
    dim_html = ""
    for dim in ["growth", "retention", "conversion", "revenue_quality", "momentum"]:
        val = getattr(health, dim)
        dim_html += f'<div class="dim"><div class="dim-label">{dim.replace("_", " ").title()}</div><div class="dim-value">{val:.0f}</div></div>\n'

    metrics_html = ""
    for name, trend in trends.items():
        change_class = ""
        change_str = "N/A"
        if trend.period_change is not None:
            change_class = "positive" if trend.period_change >= 0 else "negative"
            arrow = "↑" if trend.period_change >= 0 else "↓"
            change_str = f"{arrow} {abs(trend.period_change):.1f}%"
        metrics_html += f'''<div class="metric">
  <div class="metric-name">{trend.display_name}</div>
  <div class="metric-value">{format_value(trend.current, trend.unit)}</div>
  <div class="metric-change {change_class}">{change_str} {trend.trend_emoji}</div>
</div>\n'''

    flags_html = ""
    if flags:
        flags_items = "\n".join(f'<div class="flag">{f}</div>' for f in flags)
        flags_html = f'<div class="section"><h2>⚠️ Risk Flags</h2>{flags_items}</div>'

    recs_html = ""
    if recommendations:
        recs_items = "\n".join(f'<div class="rec">{r}</div>' for r in recommendations)
        recs_html = f'<div class="section"><h2>💡 Recommendations</h2>{recs_items}</div>'

    return HTML_TEMPLATE.format(
        project_name=project_name,
        date_range=date_range,
        generated_at=datetime.now().strftime("%Y-%m-%d %H:%M"),
        overall_score=f"{health.overall:.0f}",
        grade=health.grade,
        grade_emoji=health.grade_emoji,
        dimension_html=dim_html,
        metrics_html=metrics_html,
        flags_html=flags_html,
        recs_html=recs_html,
    )
